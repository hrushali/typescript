// Type Aliases
// Type Aliases allow defining types with a custom name (an Alias).
var carYear = 2001;
var carType = "toyota";
var carModel = "corolla";
var Car = {
    year: carYear,
    type: carType,
    model: carModel
};
console.log(Car);
var rectangle = {
    height: 20,
    width: 10
};
console.log(rectangle);
var coloredRectangle = {
    height: 20,
    width: 10,
    color: "red"
};
console.log(coloredRectangle);
